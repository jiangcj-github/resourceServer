<?php

class Random
{
    public static function getRandString($length){
        $str = null;
        $sample = "0123456789abcdefghijklmnopqrstuvwxyz";
        $max = strlen($sample)-1;
        for($i=0;$i<$length;$i++){
            $str.=$sample[rand(0,$max)];
        }
        return $str;
    }
}
